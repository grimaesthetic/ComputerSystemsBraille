package computersystemsbraille;
public class ComputerSystemsBraille {

    public static void main(String[] args) {
       //Unicode Braille
       //https://www.unicode.org/charts/PDF/U2800.pdf
       //English to Braille translator https://www.brailletranslator.org/
       
       String L= "\u2838";
       String e= "\u2811";
       String x= "\u28D2";
       String i= "\u28DA";
       
        System.out.println(L + e + x + i);
               
    }
    
}
